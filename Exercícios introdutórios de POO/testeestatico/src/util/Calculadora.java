package util;

public class Calculadora {
	

	public static final double pi = 3.14159;
	
	public static double circunferencia (double raio) {
		
		return 2.0 * pi * raio;
		
	}
	
	
	public static double volume(double raio) {
		
		return 4.0 * pi * raio * raio * raio / 3.0;
		
	}
	
}
